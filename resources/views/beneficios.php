1. Ahorrarán tiempo y dinero en el traslado hacia la oficina
Eliminando este tiempo vas a aumentar la productividad de tus empleados y les harás gastar menos dinero en gasolina o transporte público.

2. Serán más productivos
Un estudio de Stanford arrojó que los empleados que trabajan desde casa son un 13 por ciento más productivos a comparación de aquellos que no lo hacen.

3. Serán más felices
Todo el mundo habla sobre la importancia del equilibrio entre el trabajo y la vida personal. No hay mejor manera de impulsar ese balance que dejar laborar a tus trabajadores desde la comodidad de su casa.

4. Es más fácil que nunca
Es cierto que hace veinte años el concepto de “trabajar desde casa” podría escucharse “disparatado”, pero ya estamos en el siglo XXI y puedes estar conectado a la oficina cada minuto del día gracias al internet.

Aparte, la existencia de herramientas como GoToMeeting y Skype hacen que la comunicación se dé de manera extremadamente sencilla.

5. Te ahorrarás el pago de espacio
Trabajar desde el hogar significa: no oficina, o al menos, mucho menos espacio.

Si no quieres hacer un cambio de ambiente tan drástico, haz que mínimo unos días tus trabajadores lleven a cabo sus pendientes desde casa.

Automattic destina tu dinero ahorrado en el lugar físico para financiar el presupuesto de un posible gran viaje.

6. No tendrás que pagar por suministros de oficina
A nadie le gusta hacer los pedidos “a granel” de la empresa. Teniendo a tus empleados trabajando desde su casa podrás recortar gran parte de esos gastos que pueden llegar a ser algo inútiles.

7. No tendrás que gastar en “snacks”
Lo mismo sucede con esta “inversión”. IBM ha ahorrado alrededor de 100 millones de dólares anuales desde que empezó su programa de trabajo a distancia.

8. No perderás más tiempo en juntas innecesarias
¿Recuerdas cómo hoy todo se basa en la productividad? Bueno, mucha de ésta obtendrás si reduces todas esas juntas que solo hacen perder el tiempo.

9. Puedes contratar a los mejores, sin importar dónde estén
No tener oficina significa poder trabajar con cualquier persona alrededor del mundo. Redes como Toptal han capturado este concepto conectando las compañías top con el mejor software.

10. Disminuirá la posibilidad de que tus empleados renuncien
Trabajar desde casa hará que tus empleados sean felices y evitará que quieran salirse de tu compañía.

11. Obtendrás buenas Relaciones Públicas
Cuando Yahoo decidió ponerle fin a su programa de trabajo desde casa, las malas reacciones se hicieron notar. Así que si tú sustituyes el sistema ordinario de labor por uno desde el hogar verás cómo las noticias te alabarán.

12. Hay muchos servicios de gestión de producto para equipos a distancia
Herramientas como Basecamp y Asana facilitan la administración de la gente a distancia haciendo que sientas éstos justo en tu oficina.

13. Puedes obtener mayor información sobre otros mercados
El tener trabajadores alrededor del mundo te da una perspectiva del mercado mucho más abierta, beneficiándote a conocer nuevas tendencias.

14. Los empleados de otras compañías se pondrán celosos
Incluso podrían renunciar a sus trabajos para ser parte de tu empresa.b

15. No tienes que ser tan extremista
Si crees que en tu empresa trabajar desde casa –un 100 por ciento– no es la opción, intenta brindarle a los empleados la oportunidad de laborar desde su hogar aunque sea una vez a la semana o tal vez un par de días al mes.

Aun así obtendrás la mayoría de los beneficios.  Éstas son 100 compañías que ofrecen esta forma de trabajo.

16. Aumentará la lealtad de tus empleados
Nadie que tenga la oportunidad de trabajar desde su casa querrá arruinarla.

Brindarle esta confianza a tus trabajadores hará que se sientan comprometidos a entregar buenos resultados.

17. Pedirán menos días libres “por enfermedad”
Es cierto que la falta de salud puede evitar que los empleados no asistan a la oficina, pero ¿por qué no trabajar desde la comodidad del hogar?

Además, así no contagiarán al resto de sus compañeros.

18. La urgente necesidad de tomarse unas vacaciones disminuirá
Trabajar desde casa puede sentirse como un “break” de la oficina –aunque estés trabajando–.

De esta manera los empleados recargaran energía y podrán pasar más tiempo con sus familias, por lo que no sentirán esa obligación de tomarse unas vacaciones de dos semanas.

19. Los empleados trabajarán más
¿Te acuerdas del traslado para ir a la empresa? Bueno pues evitándolo estarás en tu oficina desde el minuto uno que despiertes, aprovechando así todo momento para laborar en serio. Además, esta opción evitará que se sientan explotados.

20. Tener empleados que trabajen desde casa significa que tú también lo podrás hacer
¿Sabes qué es lo mejor de todo? Tú como jefe puedes tener todos los beneficios de esta lista junto con tus trabajadores.